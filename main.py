#!/usr/bin/env python3
# ５つの「はい / いいえ」だけで診断するシンプル占い
# 10 種類の結果が必ず得られるよう、答えの組み合わせ（0-31）を 10 で割った余りで振り分けています。

questions = [
    "Q1 今日は早起きしましたか？（はい / いいえ）: ",
    "Q2 朝ごはんを食べましたか？（はい / いいえ）: ",
    "Q3 最近、運動していますか？（はい / いいえ）: ",
    "Q4 新しいことに挑戦したい気分ですか？（はい / いいえ）: ",
    "Q5 今の気分は最高ですか？（はい / いいえ）: "
]

fortunes = [
    "◆ 大吉 ◆ 今日は何をやってもうまくいく最高の日！",
    "◆ 中吉 ◆ 良い流れが来ています。チャンスを逃さず行動を。",
    "◆ 小吉 ◆ 少しの努力で成果が出そう。コツコツが鍵。",
    "◆ 吉 ◆ 穏やかな一日。リラックス重視で◎。",
    "◆ 末吉 ◆ 地道な努力が実を結びます。継続は力なり。",
    "◆ 凶 ◆ 無理は禁物。休息と安全第一で。",
    "◆ 大凶 ◆ トラブル注意報。慎重に、計画的に動こう。",
    "◆ 半吉 ◆ 感謝の気持ちが運気アップのヒント。",
    "◆ 末小吉 ◆ 小さな幸せが見つかりそう。周りをよく見て。",
    "◆ 平 ◆ いつも通りが一番。平穏な日常を楽しんで。"
]

score = 0
for i, q in enumerate(questions):
    while True:
        ans = input(q).strip()
        if ans in ("はい", "いいえ"):
            if ans == "はい":
                score |= (1 << i)      # ビットに変換してスコア化
            break
        else:
            print("→『はい』か『いいえ』で答えてください。")

result = fortunes[score % 10]

print("\n=== 今日の運勢 ===")
print(result)